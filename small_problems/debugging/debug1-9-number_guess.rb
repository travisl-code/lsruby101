# The problem with this program is the attempt to call the method recursively. Because the variable
# `winning_number` is initialized as the first line of the method, it is reinitialized every time the 
# method is called (so you don't take multiple guesses at the same number). The other problem is that 
# the number of `max_attempts` doesn't decrement at all, so you never reach the end of guesses.

